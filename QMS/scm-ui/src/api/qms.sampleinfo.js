import httpService from '@/libs/service'

export default {
	add: function (data) {
        return httpService.post('/sampleInfo/add', data)
    },
    delete: function (data) {
        return httpService.post('/sampleInfo/delete', data)
    },
    edit: function (data) {
        return httpService.post('/sampleInfo/edit', data)
    },
    getlist: function (data) {
        return httpService.get('/sampleInfo/getlist', data)
    },   
    getrecords: function (data) {
        return httpService.get('/sampleRecord/getlist', data)
    }
}