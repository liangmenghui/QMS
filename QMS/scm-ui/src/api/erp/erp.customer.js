import httpService from '@/libs/service'

export default {
    getlist: function (data) {
        return httpService.get('/erp/customer/getlist', data)
    },
    add: function (data) {
        return httpService.post('/erp/customer/add', data)
    },
    edit: function (data) {
        return httpService.post('/erp/customer/edit', data)
    },
    delete: function (data) {
        return httpService.post('/erp/customer/delete', data)
    },
    download: function (data) {
        return httpService.download('/erp/customer/download', data)
    }
}